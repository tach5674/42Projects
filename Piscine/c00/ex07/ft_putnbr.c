#include "unistd.h"

void	ft_write(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int		d;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb = -nb;
		write(1, "-", 1);
	}
	d = 1;
	while (nb / d >= 10)
	{
		d *= 10;
	}
	while (d >= 1)
	{
		ft_write(nb / d + '0');
		nb = nb % d;
		d /= 10;
	}
}
