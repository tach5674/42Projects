#include "unistd.h"

void	ft_print_comb(void)
{
	char	nums[3];

	nums[0] = '0';
	while (nums[0] <= '7')
	{
		nums[1] = nums[0] + 1;
		while (nums[1] <= '8')
		{
			nums[2] = nums[1] + 1;
			while (nums[2] <= '9')
			{
				write(1, &nums, 3);
				if (nums[0] != '7')
				{
					write(1, ", ", 2);
				}
				nums[2]++;
			}
			nums[1]++;
		}
		nums[0]++;
	}
}
